    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
            'nama' => 'required|string|max:100',
            'atas_nama' => 'required|string|max:100',
            'norek' => 'required|string|max:100',
        ]);

        $bank = Bank::findOrFail($id);
        $bank->nama = $request->input('nama');
        $bank->atas_nama = $request->input('atas_nama');
        $bank->norek = $request->input('norek');
        $bank->save();

        return redirect()->route('admin.bank.utama')->with('success', 'Bank data updated successfully.');
    }

